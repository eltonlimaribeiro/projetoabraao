        <p>“Sempre que abro os olhos pela manhã, eu sinto a presença de nosso pai”, disse Bae (nome alterado por motivo de segurança), que começa cada manhã em uma cabana rústica de uma vila rural em algum lugar nas montanhas da Coreia do Norte. O marido dela está cansado por não ter dormido e, de dentro de casa, ela ouve o sussurro de outras pessoas se preparando para outro dia de trabalho no campo.</p>
        <p>Ela espera atingir sua meta de trabalho na colheita das plantações. A cristã não quer correr o risco de receber uma punição adicional. O governo oferece comida — mas apenas o suficiente para mantê-los vivos para trabalhar. Ela sabe que o café da manhã não será suficiente para aliviar sua fome.</p>
        <p>Como todas as outras pessoas na vila, Bae está faminta. Ela não está com fome o suficiente para morrer, pelo menos não ainda. Mas a fome está sempre lá, lhe corroendo. Quando Bae consegue fazer uma pequena pausa, vai para a floresta. Os cogumelos e plantas que colhe ali ajudam a conter o ronco do estômago. Mas essa comida extra nunca é garantida.</p>
        <p>Finalmente, ao anoitecer, ela encerra o dia de trabalho e ganha outra refeição - uma sopa aguada. Caso tenha sorte, também receberá um pouco de arroz. Ao voltar para casa, Bae começa o trabalho de verdade. Ela espera a lua se esconder atrás das nuvens e então, silenciosamente, coloca uma capa. A cristã passa pela porta, com cuidado para fechá-la em silêncio, sem que nenhum dos vizinhos a ouça.  </p>
        <p>Enquanto caminha pela vila, se esconde nas sombras e segue de volta para a floresta. Mas, desta vez, não está à procura de alimento, mas atrás de algo que é ainda mais importante. Ela encontra a árvore com raízes retorcidas e limpa uma fina camada de poeira. Bae tira a Bíblia, coloca debaixo da capa e volta para sua cabana, tão quieta quanto saiu. Ao chegar em casa, seus companheiros a estão esperando. Eles já cobriram as janelas com cobertores e acenderam pequenas velas. Bae abre a Bíblia e começa a ler, com a voz um pouco mais alta do que um sussurro: “Nem só de pão viverá o homem, mas de toda palavra que procede da boca de Deus”. Esse é apenas mais um dia na igreja norte-coreana. Bae nem sempre viveu nessa vila. Ela foi mandada para lá depois que ela e o marido foram pegos com uma Bíblia. Assim que as autoridades norte-coreanas descobriram que o casal era cristão, a vida como conheciam acabou. </p>
        <p>O casal foi colocado na “categoria hostil” na Coreia do Norte, o que significa que está no degrau mais baixo quando se trata da ajuda do governo, atribuições de trabalho ou qualquer outra assistência oficial. Eles foram levados para uma vila remota e obrigados a fazer um trabalho agrícola árduo. Além disso, sabem que viverão nesse lugar até morrer. Bae disse: “O que mais tenho que agradecer é que o pai me usa para o seu trabalho. Eu quero dedicar minha vida até a morte para glorificá-lo”.</p>
        
