        <p>O livro “Escape from Camp 14”, na edição em inglês, foi publicado em português com o título “Fuga do Campo 14”. O livro conta a impressionante história do prisioneiro norte coreano Shin Dong-hyuk, que nasceu e cresceu em um campo de concentração número 14, um dos imensos complexos destinados a presos políticos da Coreia. Sua história se tornou importante, pois ele é o único fugitivo sobrevivente desses campos, classificado como “zona de controle total”.</p>
        <p>A Coreia do Norte é, possivelmente, o regime mais repressivo que tenha existido na face da terra. Isolada nação, que, por mais de 21 anos ocupa a posição número um na classificação da Missão Portas Abertas de países por perseguição.</p>
        <p>Apesar do governo norte coreano negar a existência desses campos de concentração, eles podem ser facilmente encontrados via satélite através do Google Earth para qualquer pessoa que tenha conexão de internet. Acredita-se que a Coreia mantenha 70.000 cristãos presos em 12 campos de concentração, espalhados pelo país. O livro relata com detalhes         como vivem os residentes desses campos. Condenados a trabalhar até 15 horas por dia, sofrendo fome e frio, sujeitos a uma rotina de violências sumárias. Para complementar a ração que recebem para comer, os presos consumem qualquer coisa comestível, incluindo cobras, ratos, insetos, raízes e ervas. Shin conta ainda que denunciou sua mãe e seu irmão mais velho aos policiais e assistiu, aos 13, anos à execução deles, pela tentativa de fuga do campo. A mensagem foi clara para Shin: “daqui ninguém foge com vida”.</p>
        <p>Determinado a descobrir como é a vida do outro lado da cerca eletrificada, Shin supera todo tipo de dificuldade e, arriscando a própria vida, consegue deixar a Coreia do Norte. O livro relata com detalhes a impressionante trajetória de Shin rumo a liberdade. Shin, que hoje vive na Califórnia (EUA), não conseguem se livrar das marcas do passado, facilmente           visíveis em seu corpo e mente.</p>
        <p>Recomendo fortemente a leitura desse livro a todos aqueles que amam os norte coreanos e oram por eles. O livro pode ser encontrado em diversas livrarias brasileiras pelo preço de 35 reais. Lançado pela Editora Intrinseca, o livro de  232 páginas foi compilado pelo jornalista Blaine Harden. Veja um trailer do livro no link <a href="http://www.youtube.com/watch?v=0kPR6xGKoyw" target="_blank" rel="noopener noreferrer">http://www.youtube.com/watch?v=0kPR6xGKoyw</a>.</p>
