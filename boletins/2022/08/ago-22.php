        <p>O Brasil tem se empenhado para manter relações políticas com o regime norte coreano, incluindo a abertura de uma embaixada brasileira em 2009. O Brasil também fez doação de alimentos. Contudo, infelizmente, na grande parte, serve para alimentar os vinculados do próprio governo.</p>
