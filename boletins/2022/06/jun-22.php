        <p>Seja um intercessor do povo norte coreano. A palavra do Senhor nos afirma em Jó 42:10: “Mudou o Senhor a sorte de Jó, quando este orava pelos seus amigos; e o Senhor deu-lhe o dobro de tudo o que antes possuíra.” </p>
        <p>Uma definição simples de intercessão é esta: orar pelos outros. No entanto, a intercessão vai além do que pedir a Deus algo em favor da vida de outro. De acordo com o Dicionário Oxford de Igreja Cristã, intercessão não é apenas orar em favor dos outros. Além de fazer parte das liturgias cristãs mais antigas, a intercessão também se caracteriza por fazer algo por alguém, na história do cristianismo.</p>
        <p>Ou seja, quando alguém está fraco em sua fé, é assistido por outro que intercede em seu lugar, ou em seu favor, a Deus, orando por essa pessoa (ou seja, no lugar dela, como se fosse ela), lendo a Bíblia por essa pessoa, além de realizar outros atos de devoção em lugar de alguém em uma situação específica da vida da pessoa na qual ela se encontra impossibilitada de continuar a realizar atos de piedade e devoção. Obviamente, isso tudo tem como propósito o socorro de Deus sobre a vida dessa pessoa.</p>
        <p>Não entendemos claramente como se dá esse “colocar-se no lugar de”. Só sabemos que devemos viver como um corpo, de modo que, quando um sofre, todos sofrem, quando um se alegra, todos se alegram. Assim, então, quando algum membro do corpo é impossibilitado de fazer algo em sua piedade, espera-se que outra pessoa faça em lugar daquela impossibilitada. É isso que o Espírito Santo faz por nós, quando intercede por nós “com gemidos inexprimíveis” (Rm 8.26b), em nós, como se fosse nós. “Porque não sabemos orar como convém”, o Espírito intercede em nosso lugar.</p>
        <p>A despeito dessa visão que também é contemplada na história da igreja, a intercessão ficou mais conhecida como a prática de orarmos uns pelos outros.</p>
