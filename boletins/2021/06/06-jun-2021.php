          <p><strong>PUBLICAÇÕES:</strong> O obreiro servindo na Coreia precisa ter muito cuidado com as publicações sobre seu trabalho. Deve evitar ao máximo as redes sociais e e-mails. O melhor é sempre dar relatórios pessoalmente, se possível.</p>
          <p><strong>ENTENDER A CULTURA:</strong> Os norte-coreanos podem ser considerados como crianças inocentes que são protegidos pelos pais. Eles sofrem de uma dependência grande do sistema e são proibidos de pensar, estudar e usar o seu intelecto. Com isso, adoram seus líderes na ilusão de viverem no melhor país do mundo. O obreiro precisa entender essa mentalidade inocente e se aproximar das pessoas com amor e sabedoria.</p>
          <p><strong>TRABALHO SOCIAL:</strong> Distribuir alimentos, materiais escolares, remédios, ajuda humanitária tem sido uma das melhores estratégias para quebrar as barreiras entre o obreiro e a sociedade. Lembre-se sempre que os norte-coreanos são ensinados a evitar estrangeiros, porque eles são uma maldição. Apresentar o amor de Deus através do trabalho social tem sido uma grande porta aberta para o evangelismo.</p>
          <p><strong>O QUE PRECISA SER EVITADO:</strong> As agências missionárias devem evitar enviar obreiros japoneses ou americanos na Coreia. Eles são extremamente odiados pela população. Também devem evitar qualquer crítica aos líderes ou mesmo comentários sobre a situação política do país. Os norte-coreanos precisam aos poucos ver por eles mesmos a situação deprimente em que vivem.</p>
          <p><strong>ESTRANGEIROS:</strong> Os missionários devem trabalhar com muito zelo para melhorar a imagem dos estrangeiros no país. É necessário muita paciência e amor ao povo. Críticas aos estrangeiros são constantes e diretas, por isso, o obreiro precisa mostrar através de suas palavras e atitudes que as coisas são bem diferentes do que eles estão sendo ensinados.</p>
          <p>Não é fácil trabalhar na Coreia, pois precisa haver muita renúncia por parte do obreiro. Contudo, não é impossível e com a ajuda do nosso Mestre podemos ver o fruto do nosso trabalho. Oremos por aqueles que já estão dentro da Coreia servindo a Cristo e por aqueles que estão se preparando para enfrentar um dos países mais difíceis do mundo para se trabalhar.</p>
