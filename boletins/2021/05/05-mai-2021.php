          <img class="imgComTextoContorno mb-5" src="./05-mai-2021-og.webp" width="40%" alt="01 de Maio: Dia do Trabalho e a Coreia do Norte">
          <p>Estima-se que cerca de mil trabalhadores forçados norte-coreanos trabalhem atualmente na Europa, dos quais 800 na Polônia. Empregados em canteiros de obras, estaleiros e fazendas, eles realizam trabalhos fisicamente duros e muitas vezes, perigosos. O governo de Pyongyang negocia seus contratos diretamente com as empresas estrangeiras que os empregam, e os próprios trabalhadores não recebem nenhum papel.</p>
          <p>Escravizados pelo próprio governo, os norte coreanos não sabem quanto tempo vão trabalhar e sob quais condições e são obrigados a entregar seus passaportes. E eles não recebem quase nada – estima-se que 90% de seus rendimentos sejam direcionados para os cofres do regime.</p>
          <p>Turnos de 12 horas ou mais são comuns, e os trabalhadores são acompanhados de perto por guardas da Coreia do Norte. "Eles trabalham seis dias por semana, e os domingos são preenchidos por sessões ideológicas, nas quais a presença é obrigatória", afirma Remco Breuker, professor de estudos coreanos na Universidade de Leiden, na Holanda.</p>
          <p>A exploração de trabalhadores norte-coreanos na Polônia viola uma série de leis da União Europeia (UE). "Ela viola tanto as leis polonesas quanto do bloco, os tratados e convenções da Organização Internacional do Trabalho (OIT), a Declaração de Direitos Humanos da ONU e as sanções internacionais contra a Coreia do Norte", afirma Breuker.</p>
          <p>Os trabalhadores não estão autorizados a deixar seus postos de trabalho ou socializar com a população local. Até mesmo a comunicação com as próprias famílias na Coreia do Norte é estritamente limitada. Além disso, somente aqueles que são casados e têm filhos são enviados para trabalhar no exterior, numa tentativa de minimizar o risco de fuga. E, ainda, qualquer contato com a imprensa é proibido.</p>
          <p>Jornalistas que tentam fazer reportagens sobre o assunto têm se deparado com o silêncio, com a maioria dos trabalhadores se recusando a falar por medo de represálias ao retornarem para o país natal.</p>
          <p>A União Europeia se silencia diante desses dados horrorosos. Em nota, a Comissão Europeia afirmou: “Não há o registro de nenhum trabalhador de países terceiros na União Europeia, afirmando que os Estados-membros têm suas próprias leis trabalhistas e que é da responsabilidade das autoridades e cortes nacionais garantir que as regras sejam cumpridas.</p>
          <p>Ao menos 32 empresas polonesas – de grandes estaleiros a pequenas empresas de horticultura – empregam trabalhadores da Coreia do Norte, aponta um relatório publicado pela equipe de pesquisa da Universidade de Leiden, coordenada por Breuker. Segundo o documento, os governos polonês e norte-coreano também detêm participações em algumas dessas companhias. E várias delas receberam milhões de euros em créditos da União Europeia, afirmam os pesquisadores. Assim, a Coreia do Norte tem sido beneficiada indiretamente pelos subsídios do bloco europeu, e isso é "extremamente problemático", lamentam os pesquisadores.</p>
          <p>Essa é uma triste realidade observada no solo europeu. Nesse mês de maio, onde celebramos o dia do trabalho, dia esse também celebrado na Coreia do Norte, deveríamos ter ações práticas para acabar com esse trabalho escravo, que ainda sustenta o poder de um ditador.</p>
          <p>Que Deus use nossos joelhos dobrados para mudar essa realidade!</p>
